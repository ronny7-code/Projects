    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        
        if(selectedMember == null){
            JOptionPane.showMessageDialog(this, "No record selected");
        }
        else{
            clearForm();
            addMemberButton.setText("Submit");
            fNameField.setText(selectedMember.getFirstName());
            midNameField.setText(selectedMember.getMidName());
            lastNameField.setText(selectedMember.getLastName());
            inputAddress.setText(selectedMember.getAddress());
            inputPhone.setText(selectedMember.getPhoneNo());
            inputEmail.setText(selectedMember.getEmail());
            
            switch(selectedMember.getGender()){
                case "MALE" -> buttonMale.setSelected(true);
                case "FEMALE" -> buttonFemale.setSelected(true);
                default -> buttonOthers.setSelected(true);
            }
            
            String interest = selectedMember.getInterest();
            if(interest.contains("ACADEMIC")){
                checkboxAcademic.setSelected(true);
            }
            if(interest.contains("Religion")){
                checkboxReligion.setSelected(true);
            }
            if(interest.contains("Literature")){
                checkboxLiterature.setSelected(true);
            }
            if(interest.contains("Science & Technology")){
                checkboxST.setSelected(true);
            }
            comboboxMembership.setSelectedItem(selectedMember.getMembershipType());
            
        }
        
    }//GEN-LAST:event_editButtonActionPerformed
