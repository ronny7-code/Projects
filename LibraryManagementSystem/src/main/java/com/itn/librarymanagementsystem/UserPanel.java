    private void addMemberButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMemberButtonActionPerformed
        // add member button: collect data from form
        if(!editMember){
        Member member = new Member();
        member.setFirstName(fNameField.getText());
        member.setLastName(lastNameField.getText());
        member.setMidName(midNameField.getText());
        member.setAddress(inputAddress.getText());
        member.setEmail(inputEmail.getText());
        member.setPhoneNo(inputPhone.getText());
        member.setMembershipType(String.valueOf(comboboxMembership.getSelectedItem()));
        
        
        // for radio buttons
        if(buttonMale.isSelected())
            member.setGender("MALE");
        else if(buttonFemale.isSelected())
            member.setGender("FEMALE");
        else
            member.setGender("OTHERS");
        
        // for Checkbox
        String interest = "";
        if(checkboxAcademic.isSelected())
            interest = interest.concat("ACADAMIC ");
        else if(checkboxLiterature.isSelected())
            interest = interest.concat("Literature ");
        else if(checkboxReligion.isSelected())
            interest = interest.concat("Religion ");
        else if(checkboxST.isSelected())
            interest = interest.concat("Science & Technology");
        member.setInterest(interest);
        
        boolean success = new MemberDao().addMember(member);
        if(success){
            JOptionPane.showMessageDialog(this, "Member added successfully! ");
        }
        else{
            JOptionPane.showMessageDialog(this, "Member cannot be added");
        }
        }
        
        // save data collected to a  database table..
        
        // show appropriate message..
        
    }//GEN-LAST:event_addMemberButtonActionPerformed

